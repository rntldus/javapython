package ex0627;
//CH5-1 if else 구문
public class ex04 {

	public static void main(String[] args) {
		
		int n1=10;
		int n2=20;
		
		if (n1<n2) {
			System.out.println("n1<n2 is true");
		}
		
		if (n1==n2) {
			System.out.println("n1==n2 is true");
		}
		
		else {
			System.out.println("n1==n2 is false");
		}
	}
}
